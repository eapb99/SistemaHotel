----------------------------------- CREATE ----------------------------------- 

CREATE TABLE IF NOT EXISTS hotel (
	hotel_id SERIAL PRIMARY KEY, 
	hotel_name VARCHAR(50) NOT NULL,
	country VARCHAR(50) NOT NULL,
	city VARCHAR(50) NOT NULL,
	address VARCHAR(100) NOT NULL,
	phone VARCHAR(10),
	stars_rating SMALLINT CHECK(stars_rating>=1 AND stars_rating<=5),
	num_rooms SMALLINT NOT NULL
);

CREATE TABLE IF NOT EXISTS room_type (
	room_type_id SERIAL PRIMARY KEY, 
	name VARCHAR(50) NOT NULL,
	num_beds SMALLINT NOT NULL,
	price NUMERIC(10,2) NOT NULL,
	is_vip BOOLEAN NOT NULL
);

CREATE TABLE IF NOT EXISTS room (
	room_id SERIAL PRIMARY KEY, 
	room_type_id INT NOT NULL,
	hotel_id BIGINT NOT NULL,
	code CHAR(5) NOT NULL,
	is_available BOOLEAN,
	
	CONSTRAINT fk_room_type_room FOREIGN KEY(room_type_id) REFERENCES room_type(room_type_id),
	CONSTRAINT fk_hotel_room FOREIGN KEY(hotel_id) REFERENCES hotel(hotel_id)
);

CREATE TABLE IF NOT EXISTS user_account(
	user_id SERIAL PRIMARY KEY, 
	firstname VARCHAR(50) NOT NULL,
	lastname VARCHAR(50) NOT NULL,
	gender CHAR(1) NOT NULL CHECK(gender IN('M', 'F')),
	email VARCHAR(100) NOT NULL,
	cellphone VARCHAR(15) NOT NULL UNIQUE,
	passport VARCHAR(15) NOT NULL UNIQUE,
	nationality VARCHAR(25),
	password VARCHAR(50) NOT NULL
);


CREATE TABLE IF NOT EXISTS booking (
	booking_id SERIAL PRIMARY KEY, 
	hotel_id INT NOT NULL,
	room_id INT NOT NULL,
	user_id INT NOT NULL,
	booking_date TIMESTAMP NOT NULL,
	arrival_date DATE NOT NULL,
	departure_date DATE NOT NULL,
	num_adults SMALLINT NOT NULL CHECK(num_adults>0),
	num_childrens SMALLINT,
	special_requeriments VARCHAR(500),	
	
	CONSTRAINT fk_hotel_booking FOREIGN KEY(hotel_id) REFERENCES hotel(hotel_id),
	CONSTRAINT fk_room_booking FOREIGN KEY(room_id) REFERENCES room(room_id),
	CONSTRAINT fk_user_booking FOREIGN KEY(user_id) REFERENCES user_account(user_id)
);

